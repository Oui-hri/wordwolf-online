// =========================
// ヒント取得
// =========================

export function getRandomHint(category) {
  const hints = HINTS[category];

  if (!hints || hints.length === 0) {
    return "自由に話題を広げてみよう";
  }

  return hints[
    Math.floor(Math.random() * hints.length)
  ];
}

// =========================
// プレイヤーごとにヒント配布
// できるだけ同じヒントが被らないようにする
// =========================

export function assignHints(players, category) {
  const hints = HINTS[category];

  if (!hints || hints.length === 0) {
    return players.map((player) => {
      return {
        ...player,
        hint: "自由に話題を広げてみよう"
      };
    });
  }

  const shuffledHints = [...hints].sort(() => {
    return Math.random() - 0.5;
  });

  return players.map((player, index) => {
    return {
      ...player,
      hint: shuffledHints[index % shuffledHints.length]
    };
  });
}
