function makeWordPairs(words) {
  const pairs = [];

  for (let i = 0; i < words.length; i++) {
    for (let j = i + 1; j < words.length; j++) {
      pairs.push({
        word1: words[i],
        word2: words[j]
      });
    }
  }

  return pairs;
}

function convertGroupsToTopics(groups) {
  const result = {};

  for (const category in groups) {
    result[category] = [];

    for (const subcategory in groups[category]) {
      const words = groups[category][subcategory];
      const pairs = makeWordPairs(words);

      result[category].push(...pairs);
    }
  }

  return result;
}

